#include <cstdio>
int main()
{
    int n;
    scanf("%d", &n);
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < i; j++)
        {
            printf(" "); //前半部分空格
        }
        for (int j = 0; j < 2 * (n - i) - 1; j++) //输出一行星号，第一行n=5,i=0,一共输出9个星号
        {
            printf("*");
            printf("\n"); //三角形后半部分后边的空格不用输出了直接换行
        }
    }
}
//因为输出三角形是从下到上依次奇数增长的，1，3，5，7，9，所以对于整个三角形来说，从上到下输出，先输出空格，然后星号，然后换行，本身就是对称的